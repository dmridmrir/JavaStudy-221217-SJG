package j07_반복문;

public class While {

	public static void main(String[] args) {
		
		System.out.println("프로그램 시작");
		int i = 0;
		
		
		while(i < 5) {
			System.out.println(i);
			i++;
		}
		System.out.println("프로그램 종료");
	}

}


